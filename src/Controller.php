<?php

namespace Xpressengine\Plugins\MultiLanguageManage;

use XeFrontend;
use XePresenter;
use App\Http\Controllers\Controller as BaseController;

class Controller extends BaseController
{
    public function index()
    {
        $title = 'MultiLanguageManage plugin';

        // set browser title
        XeFrontend::title($title);

        // load css file
        XeFrontend::css(Plugin::asset('assets/style.css'))->load();

        // output
        return XePresenter::make('multi_language_manage::views.index', ['title' => $title]);
    }
}
